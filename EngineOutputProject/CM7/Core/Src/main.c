uint16_t dacOut(float out){


  uint16_t outThis =(out - 0) * (4095 - 0) / (2000 - 0) + 0; //  (x - in_min) * (out_max - out_min) / (in_max - in_min) + out_min;
  HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, outThis);
  return(outThis);


}

struct Result fftCalc(float radFFT [SAMPLE_SIZE]){


	float total = 0;
////	uint8_t fftIndex = 0;

	float rate = (radFFT[112] - radFFT[0]) / SAMPLE_SIZE;
	         for ( int j = 0; j < SAMPLE_SIZE; j++) { // for loop to add all the 113 values

	        	 radFFT[j] = radFFT[j] - j * rate;

	        	 total += radFFT[j];

	         }

	        radianAvg = total / SAMPLE_SIZE; // to get the average of 113 values (1 revs)
//
	         memset(fftBufIn, 0, FFT_BUFFER_SIZE*sizeof(float));

	         for ( int o = 0; o < SAMPLE_SIZE; o++) { // for loop to add all the 113 values to the array
	      	        fftBufIn[o] = radFFT[o] - radianAvg;
//	      	        if (o >= 114) {
//	      	        	fftBufIn[o-1] = 0;
//	      	        }
	      	         }
	         arm_rfft_fast_f32(&fftHandler,fftBufIn, fftBufOut,0);

	         results.amplitude = sqrtf(((fftBufOut[14]) * (fftBufOut[14])) + ((fftBufOut[15]) * (fftBufOut[15]))) * 2 * NUM_FIRINGS/113 * radianAvg; // 6/113 = 0.05309 and 14 is real and 15 is imag

	         results.phase = atan2(fftBufOut[15], fftBufOut[14])  * 57.2958; //conversion from radians to degrees

//	         arm_cmplx_mag_f32(fftBufOut, fftResult, FFT_BUFFER_SIZE/2);
	         memset(fftBufIn, 0x00, FFT_BUFFER_SIZE*sizeof(float));
	         memset(fftBufOut, 0x00, FFT_BUFFER_SIZE*sizeof(float));

	         return results;
}


