/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */

      if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET) { // If at rising edge, save
          countAtRisingEdge = TIM2->CNT;
       }
      else { // on falling edge find period and wavelength
            period =  TIM2->CNT/60; // in seconds
            width = (TIM2->CNT - countAtRisingEdge)/60; // in seconds
            TIM2->CNT = 0; // Reset the counter
      }
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */

  /* USER CODE END TIM2_IRQn 1 */
}

/**
  * @brief This function handles TIM4 global interrupt.
  */
void TIM4_IRQHandler(void)
{
  /* USER CODE BEGIN TIM4_IRQn 0 */
  if (highPulse == 1){
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET);
    highPulse = 0;  // toggle the high pulse
    TIM4 -> CNT = TIM4 -> CNT - (2400-915); //reset the count to 2400 - 38% of 2400
  }
  else {
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_RESET);
    highPulse = 1;  // toggle the high pulse
    TIM4 -> CNT = 0; //reset the count
  }
  /* USER CODE END TIM4_IRQn 0 */
  HAL_TIM_IRQHandler(&htim4);
  /* USER CODE BEGIN TIM4_IRQn 1 */

  /* USER CODE END TIM4_IRQn 1 */
}

