/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
  // TIM2 is used here to implement a polling rate for the ADC read
  
  
  //ADC 1 should be connected to the X09-1 analog output pin
  data_in = HAL_ADC_GetValue(&hadc1); // data_in between 0=0V and 4095=3.3V, 1241 = 1V
  /*test = data_in/4;
  if (test == 0){
    TIM4 -> CCR1 = 0;
  }
  else if (test < 999){
  TIM4 -> CCR1 = test;
  }
  else{
       TIM4->CCR1 = 999;
  }*/
  
  // OLD otuput logic
  if (data_in > 4000){
    TIM4 -> CCR1 = 900; // if over 4000 in, put PWM duty cycle to 90%
  }
  else if (data_in > 3500){
    TIM4 -> CCR1 = 800; // PWM duty cycle = 80%
  }
  else if (data_in > 3000){
    TIM4 -> CCR1 = 700; // PWM duty cycle = 70%
  }
  else if (data_in > 2500){
    TIM4 -> CCR1 = 600; // PWM duty cycle = 60%
  }
  else if (data_in > 2000){
    TIM4 -> CCR1 = 500; // PWM duty cycle = 50%
  }
  else if (data_in > 1500){
    TIM4 -> CCR1 = 400; // PWM duty cycle = 40%
  }
  else if (data_in > 1000){
    TIM4 -> CCR1 = 300; // PWM duty cycle = 30%
  }
  else if (data_in > 500){
    TIM4 -> CCR1 = 200; // PWM duty cycle = 20%
  }
  else if (data_in > 250){
    TIM4 -> CCR1 = 100; // PWM duty cycle = 10%
  }
  else if (data_in < 250 ){
    TIM4 -> CCR1 = 0; // PWM duty cycle = 0%
  }
  else{
    TIM4 -> CCR1 = 0;  // turn off PWM if signal not covered under the above
  }
  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
  HAL_ADC_Start(&hadc1); // restart the ADC
  /* USER CODE END TIM2_IRQn 1 */
}

